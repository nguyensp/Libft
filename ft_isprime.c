#include "libft.h"

int	ft_isprime(int nbr)
{
	int i;

	i = 3;
	if (nbr <= 1)
		return (0);
	if (nbr % 2 == 0 && nbr > 2)
		return (0);
	while (i < (nbr / 2))
	{
		if (nbr % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
